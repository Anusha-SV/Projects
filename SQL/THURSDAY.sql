CHECK (ESTABLISHED_YEAR >1990));

CREATE TABLE BOOK(BOOK_ID INT PRIMARY KEY,TITLE VARCHAR(100) NOT NULL,AUTHOR VARCHAR(100)NOT NULL,
PUBLICATION_YEAR YEAR CHECK(PUBLICATION_YEAR>1989),LIB_ID INT,
FOREIGN KEY(LIB_ID)REFERENCES LIBRARY(LIB_ID));

